	private JasperPrint getPrint()
	{
		JasperPrint jasperPrint = AccessController.doPrivileged(
			      new PrivilegedAction<JasperPrint>() {
			          public JasperPrint run() {
			        	  JasperPrint jasperPrint = null;
						try {
							jasperPrint = (JasperPrint)JRLoader.loadObject(url);
						} catch (JRException e) {
							// TODO Auto-generated catch block
							e.printStackTrace();
						}
			        	  return jasperPrint;
			          }
			        }
			     );
		return jasperPrint;
		
	}
	public void printReport()
	{
		
		//Add your handling code here:
		if (url != null)
		{
			if (jasperPrint == null)
			{
				try
				{
//					jasperPrint = (JasperPrint)JRLoader.loadObject(url);
					jasperPrint = getPrint();
				}
				catch (Exception e)
				{
					StringWriter swriter = new StringWriter();
					PrintWriter pwriter = new PrintWriter(swriter);
					e.printStackTrace(pwriter);
					e.printStackTrace();
					swriter.write(url.getFile());
					JOptionPane.showMessageDialog(this, swriter.toString());
				}
			}
			
			if (jasperPrint != null)
			{				
				final JasperPrint print = jasperPrint;
				Thread thread = new Thread
					(
						new Runnable()
						{
							public void run()
							{
								try 
								{
									
									AccessController.doPrivileged(
										      new PrivilegedAction<Object>() {
										          public JasperPrint run() {
										        	  try {
														JasperPrintManager.printReport(print, true);
													} catch (JRException e) {
														// TODO Auto-generated catch block
														e.printStackTrace();
													}
										        	  return null;
										          }
										        }
										     );
								}
								catch (Exception e) 
								{
									StringWriter swriter = new StringWriter();
									PrintWriter pwriter = new PrintWriter(swriter);
									e.printStackTrace(pwriter);
									JOptionPane.showMessageDialog(null, swriter.toString());
								}
							}
						}
					);
				
				thread.start();
			}
			else
			{
				JOptionPane.showMessageDialog(this, "Empty report.");
			}
		}
		else
		{
			JOptionPane.showMessageDialog(this, "Source URL not specified");
		}
	}
	
	public void viewReport()
	{
		//Add your handling code here:
		if (url != null)
		{
			try
			{
				if (jasperPrint == null)
				{
					jasperPrint = getPrint();
				}
				if (jasperPrint != null)
				{
					ViewerFrame viewerFrame = new ViewerFrame(this.getAppletContext(), jasperPrint);
					viewerFrame.show();
				}
				else
				{
					JOptionPane.showMessageDialog(this, "Empty report.");
				}
			}
			catch (Exception e)
			{
				StringWriter swriter = new StringWriter();
				PrintWriter pwriter = new PrintWriter(swriter);
				e.printStackTrace(pwriter);
				JOptionPane.showMessageDialog(this, swriter.toString());
			}
		}
		else
		{
			JOptionPane.showMessageDialog(this, "Source URL not specified");
		}
	}

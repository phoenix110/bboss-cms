package com.sany.workflow.entity;

public class ProBusinessType {

	private String id;
	
	private String process_key;
	
	private String businessType_id;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getProcess_key() {
		return process_key;
	}

	public void setProcess_key(String process_key) {
		this.process_key = process_key;
	}

	public String getBusinessType_id() {
		return businessType_id;
	}

	public void setBusinessType_id(String businessType_id) {
		this.businessType_id = businessType_id;
	}
	
	
}
